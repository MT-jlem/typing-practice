#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	char	*p;
	int		l;
	int		i;

	if (!s)
		return (NULL);
	l = ft_strlen(s);
	p = (char *) malloc (l + 1);
	if (!p)
		return (NULL);
	i = 0;
	while (s[i])
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}
